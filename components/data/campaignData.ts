export enum TaskType {
  FOLLOW = 'follow',
  LIKE = 'like',
  WEBSITE = 'website',
  SHARE = 'share',
  QUIZ = 'quiz',
  INSTALL = 'install',
}

// Campaign categories enum
export enum Category {
  FITNESS = 'Fitness',
  FOOD = 'Food & Drink',
  FASHION = 'Fashion',
  TECH = 'Technology',
  ENTERTAINMENT = 'Entertainment',
  TRAVEL = 'Travel',
  BEAUTY = 'Beauty',
  EDUCATION = 'Education',
}

// Campaign statuses enum
export enum CampaignStatus {
  ACTIVE = 'active',
  TRENDING = 'trending',
  NEW = 'new',
  ENDING_SOON = 'ending_soon',
}

export enum MediaTypeStats {
  VIDEO = 'video',
  IMAGE = 'image',
}

// Interface for task
export interface Task {
  id: number;
  type: TaskType;
  description: string;
  points: number;
  url: string;
  completed: boolean;
}

// Interface for campaign
export interface Campaign {
  id: number;
  campaignName: string;
  businessName: string;
  description: string;
  points: number;
  mediaType: MediaTypeStats;
  imageUrl: string;
  videoUrl: string;
  endDate: string;
  category: Category;
  status: CampaignStatus;
  totalParticipants: number;
  tasks: Task[];
}

